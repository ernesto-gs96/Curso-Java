public class OperadoresAritmeticos {
    public static void main(String[] args){
        int numeroA = 5, numeroB = 3;

        // SUMA
        System.out.println( "La suma de " + numeroA + " + " + numeroB + " = " + ( numeroA + numeroB ) );

        // RESTA
        System.out.println( "La resta de " + numeroA + " - " + numeroB + " = " + ( numeroA - numeroB ) );

        // MULTIPLICACION
        System.out.println( "La multiplicacion de " + numeroA + " * " + numeroB + " = " + ( numeroA * numeroB ) );

        // DIVISION
        System.out.println( "La division de " + numeroA + " / " + numeroB + " = " + ( numeroA / numeroB ) );

        // POTENCIA
        System.out.println( "La potencia de " + numeroA + " ^ " + numeroB + " = " + ( numeroA * numeroB ) );
    }
}
