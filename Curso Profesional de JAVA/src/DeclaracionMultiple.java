public class DeclaracionMultiple {
    public static void main(String[] args){
        /*
        Para declara multiples variables en una sola linea, deben ser del mismo tipo y puede
        o no estar incializado.
         */
        int numero1 = 1, numero2 = 2, numero3 = 3;
        char a,b,c;

        a = 'a';
        b = 'b';
        c = 'c';

        System.out.println( numero1 + numero2 + numero3 );
        System.out.println( a + b + c );
    }
}
